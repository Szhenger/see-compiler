std::expected<void, BackendError>
CpuCCodegenTarget::emitHeaderFile(const sir::Block& block,
                                  const AllocationResult& allocation,
                                  const CodegenOptions& options) {
    std::ofstream h(output_dir_ / "model.h");
    if (!h) return std::unexpected(BackendError{"codegen", "Failed to open model.h"});

    h << std::format(R"(/* AUTO-GENERATED by SeeC++ Compiler | Target: {} */
#pragma once
#include <stddef.h>

#define SEECPP_ARENA_BYTES {}UL

#ifdef __cplusplus
extern "C" {{
#endif

// Forward Pass: Executes inference and populates intermediate activations for training.
void seecpp_forward(void* arena, const float* inputs[], float* outputs[], const void* weight_blob);

// Backward Pass: Computes adjoints (gradients) through the network graph.
void seecpp_backward(void* arena, const float* loss_gradients[], const void* weight_blob);

// Optimizer Step: Applies computed gradients to the weight footprint.
void seecpp_update_weights(void* arena, float learning_rate, void* weight_blob);

#ifdef __cplusplus
}}
#endif
)", name(), allocation.arena_bytes);

    return {};
}

std::expected<void, BackendError>
CpuCCodegenTarget::emitSourceFile(const sir::Block& block,
                                  const AllocationResult& allocation,
                                  const utility::WeightBuffer& weights,
                                  const CodegenOptions& options) {
    std::ofstream src(output_dir_ / "model.cpp");
    if (!src) return std::unexpected(BackendError{"codegen", "Failed to open model.cpp"});

    // --- File Prologue ---
    src << "/* AUTO-GENERATED by SeeC++ CpuCCodegenTarget */\n"
        << "#include \"model.h\"\n"
        << "#include <string.h>\n"
        << "#include <math.h>\n"
        << "#include <cblas.h>\n\n";

    if (options.emit_multithreaded) {
        src << "#include <omp.h>\n\n";
    }

    src << emitIm2ColHelper(options) << "\n";

    // --- Forward Pass Implementation ---
    src << "void seecpp_forward(void* arena, const float* inputs[], float* outputs[], const void* weight_blob) {\n";
    
    // 1. Declare Pointers
    src << "    /* --- Tensor Memory Bindings --- */\n";
    for (const auto& [v, slot] : allocation.allocation) {
        const char* type = slot.is_weight ? "const float*" : "float*";
        src << std::format("    {} {} = {};\n", type, sanitizeCName(v), getPointerExpression(v, allocation, "arena", "weight_blob"));
    }
    src << "\n";

    // 2. Load Inputs
    int input_idx = 0;
    for (const auto& arg : block.arguments()) {
        const auto& slot = allocation.allocation.at(arg.get());
        if (!slot.is_weight) {
            src << std::format("    memcpy({}, inputs[{}], {});\n", sanitizeCName(arg.get()), input_idx++, slot.byte_size);
        }
    }
    src << "\n    /* --- Operation Execution --- */\n";

    // 3. Dispatch Operations
    bool had_error = false;
    std::string error_msg;
    block.walk([&](const sir::Operation* op) {
        if (had_error) return;
        
        std::string mn = std::string(op->mnemonic());
        std::string code;

        if (mn == "sc_high.constant" || mn == "sc_low.reshape") {
            // No-op / Logical reinterpretation
        } else if (mn == "sc_low.im2col") {
            code = emitIm2ColOp(op, allocation);
        } else if (mn == "sc_low.matmul" || mn == "sc_high.matmul") {
            code = emitMatMulOp(op, allocation, options);
        } else if (mn == "sc_low.add" || mn == "sc_high.add") {
            code = emitAddOp(op, allocation);
        } else if (mn == "sc_high.relu" || mn == "sc_low.relu") {
            code = emitReluOp(op, allocation);
        } else if (mn == "sc_high.fused_ew") {
            code = emitFusedEwOp(op, allocation);
        } else {
            had_error = true;
            error_msg = std::format("Unsupported operation: {}", mn);
        }

        if (!code.empty()) {
            src << std::format("    /* {} */\n{}", mn, code);
        }
    });

    if (had_error) return std::unexpected(BackendError{"codegen", error_msg});

    // 4. Store Outputs (assuming last operations without uses are outputs)
    src << "\n    /* --- Output Extraction --- */\n";
    int out_idx = 0;
    block.walk([&](const sir::Operation* op) {
        if (op->mnemonic() == "sc_high.constant") return;
        for (size_t i = 0; i < op->numResults(); ++i) {
            if (op->result(i)->hasNoUses()) {
                const sir::Value* v = op->result(i);
                src << std::format("    memcpy(outputs[{}], {}, {});\n", out_idx++, sanitizeCName(v), allocation.allocation.at(v).byte_size);
            }
        }
    });

    src << "}\n\n";

    // --- Backward & Update Pass (Stubs for future Adjoint implementation) ---
    src << "void seecpp_backward(void* arena, const float* loss_gradients[], const void* weight_blob) {\n";
    src << "    /* TODO: Emit symbolic adjoint graph */\n}\n\n";

    src << "void seecpp_update_weights(void* arena, float learning_rate, void* weight_blob) {\n";
    src << "    /* TODO: Emit SGD/Adam optimizer state updates */\n}\n\n";

    return {};
}

// =============================================================================
// Helper string builders & name sanitizers
// =============================================================================

std::string CpuCCodegenTarget::sanitizeCName(const sir::Value* v) {
    std::string id(v->id());
    if (!id.empty() && id[0] == '%') id = id.substr(1);
    std::replace(id.begin(), id.end(), '.', '_');
    std::replace(id.begin(), id.end(), '-', '_');
    return "v_" + id;
}

std::string CpuCCodegenTarget::getPointerExpression(
    const sir::Value* v, const AllocationResult& alloc, std::string_view arena_var, std::string_view weights_var) {
    
    const auto& slot = alloc.allocation.at(v);
    if (slot.is_weight) {
        return std::format("(const float*)((const char*){} + {}UL)", weights_var, slot.offset);
    } else {
        return std::format("(float*)((char*){} + {}UL)", arena_var, slot.offset);
    }
}

