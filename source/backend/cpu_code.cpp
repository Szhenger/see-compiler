CpuCCodegenTarget::CpuCCodegenTarget(std::filesystem::path output_dir)
    : output_dir_(std::move(output_dir)) {}

std::expected<void, BackendError>
CpuCCodegenTarget::emit(const sir::Block& block,
                        const AllocationResult& allocation,
                        const utility::WeightBuffer& weights,
                        const CodegenOptions& options) {

    std::filesystem::create_directories(output_dir_);

    if (auto res = emitHeaderFile(block, allocation, options); !res) return res;
    if (auto res = emitSourceFile(block, allocation, weights, options); !res) return res;
    if (auto res = writeWeightsBin(weights); !res) return res;

    utility::Logger::info(std::format(
        "CpuCCodegenTarget: Successfully generated C++ Training backend artifacts in '{}'", 
        output_dir_.string()
    ));

    return {};
}

